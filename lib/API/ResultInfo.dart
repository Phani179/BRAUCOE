class ResultInfo
{
  var papCode;
  var papName;
  var papGPA;

  getPapCode()
  {
    return papCode;
  }
  getPapName()
  {
    return papName;
  }
  getPapGPA()
  {
    return papGPA;
  }

  setPapCode(var papCode)
  {
    this.papCode = papCode;
  }
  setPapName(var papName)
  {
    this.papName = papName;
  }
  setPapGPA(var papGPA)
  {
    this.papGPA = papGPA;
  }
}